function [R0,R1]= taylor_series_vaxis(h,v)
%---- TAYLOR SERIES ABOUT THE AXIS V=0 ----%
PE = zeros(1,10);
QE = zeros(1,10);
P = zeros(1,15);
Q = zeros(1,10);

H = h; V = v;
HI = -1.0/H;
VDH = V*HI;

TA= [0.0 0.333333333333 0.2 0.142857142857 0.111111111111 0.0909090909091...
    0.0769230769231 0.0666666666667 0.0588235294118 0.0526315789474];
