    -0.196380615234 0.185470581055];

HDN= 0.5*H;
PE(1)= -HDN;
QE(1)= -HDN;
for i = 2:5
    HDN= H*TA(i);
    P(i)= HDN*PE(i-1)+TC(i);
    Q(i)= HDN*QE(i-1)+TD(i);
    HDN= H*TB(i);
    PE(i)= HDN*P(i);
    QE(i)= HDN*Q(i);
end
R0 = 0;
R1 = 0;
if (V >= max(-1.5,-0.4*H+0.15))
    R0= -VDH*(1.0+VDH*(PE(1)-VDH*(P(2)-VDH*(PE(2)-VDH*(P(3)-VDH*...
                        (PE(3)-VDH*(P(4)-VDH*(PE(4)-VDH*(P(5)-VDH*(PE(5)-VDH*R0))))))))));
    R1= VDH*HI*(1.0+VDH*(QE(1)-VDH*(Q(2)-VDH*(QE(2)-VDH*(Q(3)-VDH*...
                        (QE(3)-VDH*(Q(4)-VDH*(QE(4)-VDH*(Q(5)-VDH*(QE(5)-VDH*R1))))))))));
else
    for i=6:10
        HDN= H*TA(i);
        P(i)= HDN*PE(i-1)+TC(i);
        Q(i)= HDN*QE(i-1)+TD(i);
        HDN= H*TB(i);
        PE(i)= HDN*P(i);
        QE(i)= HDN*Q(i);
    end
    R0= P(6)-VDH*(PE(6)-VDH*(P(7)-VDH*(PE(7)-VDH*(P(8)-VDH*(PE(8)...
                    -VDH*(P(9)-VDH*(PE(9)-VDH*(P(10)-VDH*PE(10)))))))));
    R1= Q(6)-VDH*(QE(6)-VDH*(Q(7)-VDH*(QE(7)-VDH*(Q(8)-VDH*(QE(8)...
                    -VDH*(Q(9)-VDH*(QE(9)-VDH*(Q(10)-VDH*QE(10)))))))));
    R0= -VDH*(1.0+VDH*(PE(1)-VDH*(P(2)-VDH*(PE(2)-VDH*(P(3)-VDH*...
                    (PE(3)-VDH*(P(4)-VDH*(PE(4)-VDH*(P(5)-VDH*(PE(5)-VDH*R0))))))))));
    R1= VDH*HI*(1.0+VDH*(QE(1)-VDH*(Q(2)-VDH*(QE(2)-VDH*(Q(3)-VDH*...
                    (QE(3)-VDH*(Q(4)-VDH*(QE(4)-VDH*(Q(5)-VDH*(QE(5)-VDH*R1))))))))));
end
    
if (H > 6)
    T = -3.0+0.3333333333*H;
    BH0= (((((((0.05540084*T-0.15817848)*T-0.62263549)*T+1.52099217)*T...
                    +1.73139101)*T-3.11563758)*T-1.05612047)*T+0.89505969)/...
                        ((((((0.00166189*T-0.00065708)*T+0.02509944)*T-0.00819426)*T...
                                +0.22348420)*T-0.04147932)*T+1.0);
    BH1 = (((((((-0.04992109*T-0.09940288)*T+0.72354842)*T+0.84854339)*T...
                    -2.75706514)*T-1.23993929)*T+2.40322266)*T+1.33966466)/...
                        ((((((0.00176336*T+0.00220414)*T+0.02691296)*T+0.02561187)*T...
                                +0.23302896)*T+0.12286542)*T+1.0);
else
    T= -1.4+0.4*H;
    BH0 = (((((((0.10697682*T+0.02861480)*T-1.25503173)*T+0.20447440)*T...
                                +3.84202492)*T-1.07621145)*T-3.32049215)*T+0.86369090)/...
                                        ((((((-0.00662773*T-0.00008241)*T-0.06241050)*T+0.00548426)*T...
                                                -0.20941371)*T+0.08904578)*T+1.0);
    BH1= (((((((-0.03380630*T-0.14624308)*T+0.59392539)*T+1.27350692)*T...
                                -1.95999365)*T-3.19509534)*T+2.01871118)*T+2.35896024)/...
                                        ((((((-0.00048497*T+0.00852158)*T+0.00402287)*T+0.11318441)*T...
                                                +0.13088493)*T+0.65471881)*T+1.0);
end

EV= ((((-0.000000351*V+0.000009652)*V-0.000326627)*V...
                +.007811604)*V-0.125000204)*V+1.0;
EV= 1.0/((EV^2)^2)^2;
R0= R0-BH0*EV;
R1= R1+(1.0-BH1)*EV;